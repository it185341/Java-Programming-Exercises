
public class SteeringSystem {
    // Starts the vehicle
    public void start() {
        System.out.println("The vehicle starts.");
    }

    // Turns off the vehicle
    public void stop() {
        System.out.println("The vehicle stops.");
    }

    // Turn the vehicle to the left
    public void turnLeft() {
        System.out.println("The vehicle turn left.");
    }

    // Turn the vehicle to the right
    public void turnRight() {
        System.out.println("The vehicle turn right.");
    }
}
